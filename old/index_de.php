<h2 id="über">Über</h2>
	<a traget="_blank" href="img/Ikarus_Pressefoto.jpg"><img src="img/Ikarus_Pressefoto_klein.jpg"/></a>

	<p>ECHO ist das erste Album der Band IKARUS um den 25-jährigen Komponisten und Schlagzeuger Ramón Oliveras. Zusammen mit Stefanie Suhner (Stimme), Andreas Lareida (Stimme), Lucca Fries (Piano) und Mo Meyer (Bass) setzt Oliveras seine meist auskomponierten Stücke konsequent um. Die Working Band versteht sich als musikalischer Organismus, in dem auch die Stimmen ohne Worte als Instrumente und Klangfarben eingesetzt werden. Zu den Einflüssen der Musik sagt Oliveras: „Bartók trifft auf James Blake, Arvo Pärt auf Eivind Aarset. Wir nennen das Resultat Contemporary Evocation Music.“</p>
	<p>So entsteht eine Musik, die sich zwischen Stille und Sturm entfaltet und ihre Energie aus tänzerischen Minimal-Grooves, psychedelischen Soundscapes und jazzverwandten Motiv- Verzahnungen gewinnt. Rohe Riffs sind ebenso Teil der musikalischen Dramaturgie wie zarte subtile Melodiegeflechte – oft wird beides gar im gleichen Song verbunden: als organische Entwicklung oder als überraschender Schnitt.</p>
	<p>Die Mitglieder von Ikarus arbeiten zusammen an einer sinnlichen post-genre Musik, die trotz der genannten Einflüsse vor allem selbstreferenziell sein will. Sie möchte unser phantastisches Hören anregen und uns in ein surreales Klangkino entführen, das unsere Träume und die Geister in uns evoziert.</p>  

