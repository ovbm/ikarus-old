<p>
	Der musikalische Organismus IKARUS ist mit den schillernden Stücken der beiden Alben Echo und Chronosome sowie brandneuen Stücken auf Tour. Die Band - bestehend aus Schlagzeug, Bass, Piano, sowie männlichem und weiblichem Gesang – ist regelmässig unterwegs in Europa und Japan, und entführt ihre Zuhörer in einen Klang-Urwald aus leuchtenden und vibrierenden Farben und Formen.</p>

	<p>IKARUS nennen ihre Musik Evocativ Jazz – und zeigen ein modernes Gebräu, bestehend aus feingliedrigen Polyrhythmen, sinnlichem Gesang und energetischen Improvisationen. Folglich nennt Bandleader und Komponist Ramón Oliveras unter anderem auch Morton Feldman, Meshuggah, The Knife und Craig Taborn im gleichen Satz als seine Inspirationen. So entsteht bisher ungehörte Musik zwischen nordischer Dunkelheit und tropischer Farbpalette.</p>

